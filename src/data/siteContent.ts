// Функция получения даты последнего понедельника
export const getLastMonday = (): string => {
  const now = new Date();
  const day = now.getDay();
  const diff = now.getDate() - day + (day === 0 ? -6 : 1);
  const lastMonday = new Date(now.setDate(diff));
  return lastMonday.toLocaleDateString("ru-RU", {
    day: "numeric",
    month: "long",
    year: "numeric",
  });
};